//Shape definitions

type circle struct {
	radius float64
}

type sphere struct {
	radius float64
}

type square struct {
	side float64
}

type cube struct {
	side float64
}

type rectangle struct {
	length, width float64
}

type rectPrism struct {
	length, width, height float64
}

type triangle struct {
	base, height float64
}

type triPrism struct {
	length, width, height float64
}

//Slices of each shape type

